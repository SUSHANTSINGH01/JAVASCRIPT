// while loop
var c=0;
console.log("\nStarting of while Loop ");
while(c<10)
{
    console.log("\nCurrent Count : " + c);
    c++;
}
console.log("\nwhile Loop stopped.\n")



// do while loop
var d=0;
console.log("\nStarting of Do while Loop ");
do
{
    console.log("\nCurrent Count : " + d)
    d++;
}while (d < 5);
    
console.log("\nDo while Loop stopped.\n");



// for loop
var i;
console.log("\nStarting of for loop ");
for(i=0;i<10;i++)
{
    console.log("\nCurrent Count : " +i);
}
console.log("\nfor Loop stopped.\n");
